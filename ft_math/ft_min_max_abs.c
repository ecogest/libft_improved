int		ft_int_min(int x, int y)
{
	return (x < y ? x : y);
}

int		ft_int_max(int x, int y)
{
	return (x > y ? x : y);
}

int		ft_int_abs(int x)
{
	return (x > 0 ? x : -x);
}
